function parseQuestions(text) {
  const lines = text
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean);
  const parsed = [];
  let section = "";
  let subsection = "";

  lines.forEach((line) => {
    if (sectionPattern.test(line)) {
      section = line;
      subsection = "";
      return;
    }
    const match = line.match(itemPattern);
    if (!match) {
      subsection = line;
      return;
    }
    const body = match[2];
    const split = splitPromptAndAnswer(body);
    if (!split) return;
    parsed.push({
      id: parsed.length + 1,
      section,
      subsection,
      prompt: split.prompt,
      prefix: split.prefix,
      answer: split.answer
    });
  });
  return parsed;
}

function shuffleQuestionsBySection(parsedQuestions) {
  const groups = [];
  parsedQuestions.forEach((question) => {
    const lastGroup = groups.at(-1);
    if (lastGroup?.section === question.section) {
      lastGroup.questions.push(question);
      return;
    }
    groups.push({ section: question.section, questions: [question] });
  });
  return groups.flatMap((group) => shuffleArray(group.questions));
}

function shuffleArray(items) {
  const shuffled = [...items];
  for (let index = shuffled.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(Math.random() * (index + 1));
    [shuffled[index], shuffled[swapIndex]] = [shuffled[swapIndex], shuffled[index]];
  }
  return shuffled;
}

function splitPromptAndAnswer(body) {
  let prompt = "";
  let answer = "";
  if (body.includes("?")) {
    const index = body.indexOf("?");
    prompt = body.slice(0, index + 1).trim();
    answer = body.slice(index + 1).trim();
  } else {
    const index = body.indexOf(".");
    if (index < 0) return null;
    prompt = body.slice(0, index + 1).trim();
    answer = body.slice(index + 1).trim();
  }
  let prefix = "";
  if (/^non,\s*/i.test(answer)) {
    prefix = "Non,";
    answer = answer.replace(/^non,\s*/i, "").trim();
  }
  if (/^oui,\s*/i.test(answer)) {
    prefix = "Oui,";
    answer = answer.replace(/^oui,\s*/i, "").trim();
  }
  return { prompt, prefix, answer };
}

